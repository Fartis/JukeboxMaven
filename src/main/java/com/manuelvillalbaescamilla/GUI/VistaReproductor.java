    public synchronized void notificarProceso(String time, int progress, String[] infos) {
        titleLabel.setText("");
        this.timeLabel.setText(time);
        this.barraProgreso.setValue(progress);
        for(String info : infos){
            titleLabel.setText(titleLabel.getText() + info);
        }
    }
