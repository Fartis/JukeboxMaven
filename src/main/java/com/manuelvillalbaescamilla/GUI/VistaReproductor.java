    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
//Creamos el objeto JFileChooser
        JFileChooser fc=new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("MP3 File","mp3");
        fc.setFileFilter(filter);

        //Abrimos la ventana, guardamos la opcion seleccionada por el usuario
        int seleccion=fc.showOpenDialog(this);

        //Si el usuario, pincha en aceptar
        if(seleccion==JFileChooser.APPROVE_OPTION){

            //Seleccionamos el fichero
            trackList.setModel(this.control.addTrack(fc.getSelectedFile()));
            
            
        }
    }//GEN-LAST:event_addButtonActionPerformed

    private void playButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playButtonActionPerformed
        // TODO add your handling code here:
        if(trackList.getSelectedIndex() == -1 && trackList.getComponentCount()>0) trackList.setSelectedIndex(0);
        control.playTrack(trackList.getSelectedIndex());
        barraProgreso.setMaximum(control.getDuration());
    }//GEN-LAST:event_playButtonActionPerformed

    private void stopButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stopButtonActionPerformed
        // TODO add your handling code here:
        control.stopTrack();
    }//GEN-LAST:event_stopButtonActionPerformed

    private void addFolderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFolderButtonActionPerformed
        // TODO add your handling code here:
        //Creamos el objeto JFileChooser
        JFileChooser fc=new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("MP3 File","mp3");
        fc.setFileFilter(filter);
        fc.setMultiSelectionEnabled(true);

        //Abrimos la ventana, guardamos la opcion seleccionada por el usuario
        int seleccion=fc.showOpenDialog(this);

        //Si el usuario, pincha en aceptar
        if(seleccion==JFileChooser.APPROVE_OPTION){
            
            File[] files = fc.getSelectedFiles();

            trackList.setModel(this.control.addTracks(files));
            
        }
    }//GEN-LAST:event_addFolderButtonActionPerformed

